/** 3자리 숫자 코드 → 대분류 섹터 레이블 */
export function getSector(code: string): string {
  const n = parseInt(code, 10)
  if (n >= 10 && n <= 39) return KSIC_SECTOR['A']
  if (n >= 50 && n <= 99) return KSIC_SECTOR['B']
  if (n >= 100 && n <= 339) return KSIC_SECTOR['C']
  if (n >= 350 && n <= 359) return KSIC_SECTOR['D']
  if (n >= 360 && n <= 389) return KSIC_SECTOR['E']
  if (n >= 410 && n <= 439) return KSIC_SECTOR['F']
  if (n >= 450 && n <= 479) return KSIC_SECTOR['G']
  if (n >= 491 && n <= 529) return KSIC_SECTOR['H']
  if (n >= 550 && n <= 569) return KSIC_SECTOR['I']
  if (n >= 581 && n <= 639) return KSIC_SECTOR['J']
  if (n >= 641 && n <= 669) return KSIC_SECTOR['K']
  if (n >= 681 && n <= 689) return KSIC_SECTOR['L']
  if (n >= 700 && n <= 739) return KSIC_SECTOR['M']
  if (n >= 740 && n <= 769) return KSIC_SECTOR['N']
  if (n >= 841 && n <= 849) return KSIC_SECTOR['O']
  if (n >= 851 && n <= 859) return KSIC_SECTOR['P']
  if (n >= 861 && n <= 879) return KSIC_SECTOR['Q']
  if (n >= 901 && n <= 919) return KSIC_SECTOR['R']
  if (n >= 941 && n <= 969) return KSIC_SECTOR['S']
  if (n >= 970 && n <= 989) return KSIC_SECTOR['T']
  return code
}
